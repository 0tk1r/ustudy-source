//Простой switch-case
$number = 1;
switch ($number) {
    case 1:
        echo "Var: 1";
        break;
    case 2:
        echo "Var: 2";
        break;
    case 3:
        echo "Var: 3";
        break;
    case 4:
        echo "Var: 4";
        break;
    case 5:
        echo "Var: 5";
        break;
    default:
        echo "default";
}
