 # Логичиские операторы:

 /*
 В php логические операторы используются для выполнения логических операций между
 выражениями. Они позволяют проверять условия, объединять выражения и изменять их
 значение. Рассмотрим основные логические операторы в PHP:
 */

 // 1) Логическое "И" (AND, &&)
 // AND и && проверяют, что оба выражения истинны.
 // Если оба выражения истинны, возвращается true. В противном случае – false.
 // Разница между AND и && в приоритете выполнения: && имеет более высокий приоритет.
// Пример:

$bool1 = true;
$bool2 = false;

$result1 = $bool1 && $bool2; // false, так как $b ложно
$result2 = $bool1 AND $bool2; // false, работает так же, но имеет меньший приоритет

echo "&& результат: " . ($result1 ? "true" : "false") . "<br>";
echo "AND результат: " . ($result2 ? "true" : "false") . "<br>";

 // 2) Логическое "ИЛИ" (OR, ||)
 // OR и || проверяют, что хотя бы одно из выражений истинно
 // Если хотя бы одно выражение истинно, возвращается true. Если оба ложны, возвращается false.
 // Разница между OR и || также заключается в приоритете: || имеет более высокий приоритет.
 //Пример:

$a = true;
$b = false;

$result1 = $a || $b; // true, так как $a истинно
$result2 = $a OR $b; // true, работает так же, но имеет меньший приоритет

echo "|| результат: " . ($result1 ? "true" : "false") . "<br>";
echo "OR результат: " . ($result2 ? "true" : "false") . "<br>";

